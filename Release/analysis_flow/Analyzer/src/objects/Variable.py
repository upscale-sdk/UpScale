class Variable:	
	def __init__(self, rowID, name, type="String", value="", color="#000000", description=""):
		self.rowID = rowID
		self.name = name
		self.type = type
		self.value = value
		self.selectedValue = ""
		if (self.type == "String" or self.type == "Password"): self.selectedValue = value
		elif self.type == "List": self.selectedValue = str(value).split(" ; ")[0]		
		self.color = color		
		self.description = description	
